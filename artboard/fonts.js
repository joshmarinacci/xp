export function num_to_double_digits(hours) {
    let chars = []
    if(hours < 10) {
        chars.push(0)
        chars.push(hours%10)
    } else {
        chars.push(Math.floor(hours/10))
        chars.push(hours%10)
    }
    return chars
}

